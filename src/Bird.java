import java.awt.*;

public class Bird {
    public int x;
    public int y;
    public int width;
    public int height;
    public Image image;

    Bird(int x, int y, int width, int height, Image image) {
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;
        this.image = image;
    }

}
